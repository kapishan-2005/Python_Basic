number = {"one": 1, "two": 2, "three": 3}#create a dictionary vale
print(number)#print entire dictionary
print(number["one"])#access value using key
print(number.get("two"))#using get method
num=number["three"]#access value and store in variable
               #default methods for dictionary
print(num)#print value stored in variable
print(number.keys())#print all keys in dictionary
print(number.values())#print all values in dictionary
print(number.items())#print all items in dictionary
