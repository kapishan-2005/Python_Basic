rem = {"one", "two", "three",2,3,4}#create a set value
rem.remove("two")#remove element using remove() method
print(rem)#print set after removing element
rem.remove(3)#remove element using remove() method
print(rem)#print set after removing element
rem.discard("three")#remove element using discard() method
print(rem)#print set after removing element